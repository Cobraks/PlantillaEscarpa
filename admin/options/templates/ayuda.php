<h1>Ayuda</h1>
<h2>Introducción</h2>
    <p>Este plugin te permite gestionar el stock de coches de segunda mano en tu sitio web. Con él, puedes elegir el número de coches a mostrar, cómo ordenarlos y personalizar su apariencia.</p>
    <h2>Funciones principales</h2>
    <ul>
        <li><strong>Gestión de stock:</strong> Elige el número de coches a mostrar y cómo ordenarlos.</li>
        <li><strong>Optimización de WordPress:</strong> Elimina las cosas innecesarias para mejorar el rendimiento de tu sitio.</li>
        <li><strong>Personalización:</strong> Personaliza el color y la apariencia de los coches en tu sitio.</li>
        <li><strong>Generación de Schema:</strong> Modifica la generación del script JSON ofrecido por Yoast para adaptarlo a tus necesidades.</li>
        <li><strong>Datos de contacto:</strong> Cambia los datos de contacto en un solo lugar para que se actualicen en todo el sitio.</li>
        <li><strong>Tratamiento de imágenes:</strong> Optimiza la carga de imágenes en tu sitio utilizando la carga en diferido.</li>
        <li><strong>Optimización del servidor:</strong> Mejora el rendimiento del servidor utilizando técnicas avanzadas de optimización.</li>
    </ul>
    <h2>Preguntas frecuentes</h2>
    <h3>¿Cómo puedo cambiar el número de coches a mostrar?</h3>
    <p>Puedes cambiar el número de coches a mostrar en la página de opciones del plugin. Ve a la sección "Gestión de stock" y selecciona el número deseado en el campo correspondiente.</p>
    <h3>¿Cómo puedo personalizar el color de los coches?</h3>
    <p>Puedes personalizar el color de los coches en la página de opciones del plugin. Ve a la sección "Personalización" y selecciona el color deseado en el campo correspondiente.</p>
    <h3>¿Cómo puedo modificar la generación del script JSON ofrecido por Yoast?</h3>
    <p>Puedes modificar la generación del script JSON ofrecido por Yoast en la página de opciones del plugin. Ve a la sección "Generación de Schema" y selecciona las opciones deseadas en los campos correspondientes.</p>
    <h3>¿Cómo puedo cambiar los datos de contacto?</h3>
    <p>Puedes cambiar los datos de contacto en la página de opciones del plugin. Ve a la sección "Datos de contacto" y cambia los datos en los campos correspondientes.</p>
    <h3>¿Cómo puedo optimizar la carga de imágenes en mi sitio?</h3>
    <p>Puedes optimizar la carga de imágenes en tu sitio utilizando la función de carga en diferido proporcionada por el plugin. Ve a la sección "Tratamiento de imágenes" y activa la opción "Carga en diferido" para habilitar esta función.</p>
    <h3>¿Cómo puedo mejorar el SEO de mi sitio con este plugin?</h3> <p>Puedes utilizar las funciones de optimización de recursos y velocidad proporcionadas por el plugin para mejorar el SEO de tu sitio. También puedes utilizar la función de generación de Schema para agregar datos estructurados a tus páginas y mejorar su visibilidad en los motores de búsqueda.</p>

    <h3>¿Cómo puedo gestionar el inventario de vehículos con este plugin? </h3><p>Puedes utilizar la sección “Gestión de stock” en la página de opciones del plugin para gestionar el inventario de vehículos en tu sitio. Puedes agregar, editar o eliminar vehículos y personalizar su información y apariencia.</p>

    <h3>¿Cómo puedo personalizar la apariencia de los vehículos en mi sitio?</h3> <p>Puedes personalizar la apariencia de los vehículos en tu sitio utilizando las opciones disponibles en la sección “Personalización” en la página de opciones del plugin. Puedes cambiar el color, el tamaño y otros aspectos visuales de los vehículos para adaptarlos a tus necesidades.</p>

    <h3>¿Cómo puedo optimizar el rendimiento del servidor con este plugin?</h3><p> Puedes utilizar las funciones de optimización del servidor proporcionadas por el plugin para mejorar el rendimiento del servidor y reducir el tiempo de carga de tus páginas. Puedes activar o desactivar diferentes opciones de optimización en la sección “Optimización del servidor” en la página de opciones del plugin.</p>

    <h3>¿Cómo puedo obtener más ayuda o soporte para este plugin?</h3> <p>Si necesitas más ayuda o soporte para utilizar este plugin, puedes visitar nuestra página web o ponerte en contacto con nuestro equipo de soporte. También puedes consultar nuestra documentación o participar en nuestros foros para obtener más información y ayuda.</p>
